  function normalizeLocale(value) {
    const raw = String(value || '').trim();
    if (!raw) return DEFAULT_LOCALE;
    const exact = SUPPORTED_LANGUAGES.find(([locale]) => locale.toLowerCase() === raw.toLowerCase());
    if (exact) return exact[0];
    const language = raw.split(/[-_]/)[0].toLowerCase();
    if (language === 'zh') return 'zh-CN';
    const broad = SUPPORTED_LANGUAGES.find(([locale]) => locale.toLowerCase().startsWith(language));
    return broad ? broad[0] : DEFAULT_LOCALE;
  }

  function interpolate(text, params) {
    return String(text).replace(/\{(\w+)\}/g, (_, key) => String(params?.[key] ?? ''));
  }

  function t(key, fallback, params) {
    const localeTable = DICTIONARY[currentLocale] || {};
    const text = localeTable[key] ?? EN[key] ?? fallback ?? key;
    return interpolate(text, params);
  }

  function detectLocale() {
    try {
      const stored = String(window.localStorage.getItem(STORAGE_KEY) || '').trim();
      if (stored) return normalizeLocale(stored);
    } catch {}
    const languages = Array.isArray(window.navigator?.languages) ? window.navigator.languages : [window.navigator?.language];
    return normalizeLocale(languages.find(Boolean));
  }

