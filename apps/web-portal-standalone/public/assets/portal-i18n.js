  function normalizeLiteral(text) {
    return String(text || '').replace(/\s+/g, ' ').trim();
  }

  function applyLiteralTranslations(root = document) {
    const literalTable = STATIC_LITERAL_TRANSLATIONS[currentLocale] || {};
    if (!Object.keys(literalTable).length) return;
    root.querySelectorAll('summary,h2,h3,p,button,option,.kicker,.muted,.empty-state').forEach((element) => {
      if (element.hasAttribute('data-i18n') || element.hasAttribute('data-i18n-placeholder') || element.hasAttribute('data-i18n-alt')) {
        return;
      }
      const text = normalizeLiteral(element.textContent);
      if (!text) return;
      const translated = literalTable[text];
      if (translated) {
        element.textContent = translated;
      }
    });
  }

  function applyTranslations(root = document) {
    root.querySelectorAll('[data-i18n]').forEach((element) => {
      element.textContent = t(element.dataset.i18n, element.textContent);
    });
    root.querySelectorAll('[data-i18n-placeholder]').forEach((element) => {
      element.setAttribute('placeholder', t(element.dataset.i18nPlaceholder, element.getAttribute('placeholder') || ''));
    });
    root.querySelectorAll('[data-i18n-alt]').forEach((element) => {
      element.setAttribute('alt', t(element.dataset.i18nAlt, element.getAttribute('alt') || ''));
    });
    applyLiteralTranslations(root);
    document.documentElement.lang = currentLocale;
  }

  function syncSelectors() {
    document.querySelectorAll('[data-language-select]').forEach((select) => {
      const currentValue = String(select.value || '').trim();
      select.innerHTML = SUPPORTED_LANGUAGES.map(([locale, label]) => `<option value="${locale}">${label}</option>`).join('');
      select.value = normalizeLocale(currentValue || currentLocale);
    });
  }

  function setLocale(locale, options = {}) {
    currentLocale = normalizeLocale(locale);
    if (options.persist !== false) {
      try {
        window.localStorage.setItem(STORAGE_KEY, currentLocale);
      } catch {}
    }
    syncSelectors();
    applyTranslations(document);
    window.dispatchEvent(new CustomEvent('ui-language-change', { detail: { locale: currentLocale } }));
  }

  function initSelector(selectId) {
    const select = document.getElementById(selectId);
    if (!select) return;
    select.setAttribute('data-language-select', 'true');
    syncSelectors();
    select.value = currentLocale;
    select.addEventListener('change', () => setLocale(select.value));
  }

  function initialize(selectIds = []) {
    currentLocale = detectLocale();
    selectIds.forEach((id) => initSelector(id));
    applyTranslations(document);
  }

  window.PortalUiI18n = {
    getLocale() {
      return currentLocale;
    },
    t,
    translateLiterals: applyLiteralTranslations,
    setLocale,
    init: initialize,
  };
